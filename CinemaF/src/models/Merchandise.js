export class Merchandise {
  constructor(id, name, price, imageUrl, size, material, type, count, status) {
    this.id = id
    this.name = name
    this.price = price
    this.imageUrl = imageUrl
    this.size = size
    this.material = material
    this.type = type
    this.count = count
    this.status = status
  }
}